const audio1 = document.getElementById('audio1');
const play1 = document.getElementById('play1');
const coverImg1 = document.getElementById('cover-img1');

play1.addEventListener('click', () => {
    if (audio1.paused) {
        audio1.play();
        play1.src = "./images/stop.png";
        coverImg1.classList.add('black-and-white', 'rotating');
    } else {
        audio1.pause();
        play1.src = "./images/play.png";
        coverImg1.classList.remove('black-and-white', 'rotating');
    }
});

